    public int OnHearSpeech(obj_id self, obj_id speaker, String text) throws InterruptedException
    {
        if (speaker != self)
        {
            return SCRIPT_CONTINUE;
        }
        text = toLower(text);
        if (text.startsWith("showparticles"))
        {
            String filter = "all";
            String filter2 = "all";
            StringTokenizer st = new StringTokenizer(text);
            int tokenCount = st.countTokens();
            if (tokenCount > 1)
            {
                st.nextToken();
                filter = st.nextToken();
            }
            else if (utils.hasScriptVar(speaker, "particlebrowser.filter"))
            {
                filter = utils.getStringScriptVar(speaker, "particlebrowser.filter");
            }
            if (tokenCount > 2)
            {
                filter2 = st.nextToken();
            }
            else if (utils.hasScriptVar(speaker, "particlebrowser.filter2"))
            {
                filter2 = utils.getStringScriptVar(speaker, "particlebrowser.filter2");
            }
            if (filter.length() < 1)
            {
                filter = "all";
            }
            if (filter2.length() < 1)
            {
                filter2 = "all";
            }
            utils.setScriptVar(speaker, "particlebrowser.filter", filter);
            utils.setScriptVar(speaker, "particlebrowser.filter2", filter2);
            Vector itms = new Vector();
            for (String particle : particles) {
                if ((filter.equals("all") || particle.contains(filter)) && (filter2.equals("all") || particle.contains(filter2))) {
                    itms.add(particle);
                }
            }
            sendSystemMessageTestingOnly(speaker, "Found " + itms.size() + " particles that fit your filters of '" + filter + "' and '" + filter2);
            showWindow(speaker, itms);
            utils.setBatchScriptVar(speaker, "particlebrowser", itms);
        }
        return SCRIPT_CONTINUE;
    }
    public int msgParticleBrowser(obj_id self, dictionary params) throws InterruptedException
    {
        if ((params == null) || (params.isEmpty()))
        {
            return SCRIPT_CONTINUE;
        }
        obj_id player = sui.getPlayerId(params);
        int btn = sui.getIntButtonPressed(params);
        int idx = sui.getListboxSelectedRow(params);
        if (btn == sui.BP_CANCEL)
        {
            cleanScriptVars(player);
            closeOldWindow(player);
            return SCRIPT_CONTINUE;
        }
        Vector itms = utils.getResizeableStringBatchScriptVar(player, "particlebrowser");
        if (idx >= itms.size())
        {
            cleanScriptVars(player);
            closeOldWindow(player);
            return SCRIPT_CONTINUE;
        }
        utils.setScriptVar(player, "particlebrowser.index", idx);
        String particle = (String)itms.get(idx);
        obj_id target = getLookAtTarget(player);
        if (!isIdValid(target))
        {
            target = self;
        }
        playClientEffectLoc(target, "appearance/" + particle, getLocation(target), 0.0f);
        sendSystemMessageTestingOnly(player, "Playing particle " + particle);
        int startIdx = 0;
        if (utils.hasScriptVar(player, "particlebrowser.index"))
        {
            
        }
        
        {
            startIdx = utils.getIntScriptVar(player, "particlebrowser.index");
        }
        while (startIdx > 0)
        {
            itms.add(itms.get(0));
            itms.removeElementAt(0);
            startIdx--;
        }
        utils.setBatchScriptVar(player, "particlebrowser", itms);
        showWindow(player, itms);
        return SCRIPT_CONTINUE;
    }
